/**
 * Studio 3D/4D Pro — ZIP Export Override
 * Remplace exportZIP avec version professionnelle bilingue
 * Inclus APRÈS script_0.js dans index.html
 */

window.exportZIP = async function() {
  if (window.SketchExtruder && typeof window.SketchExtruder.exportScene === 'function') {
    window.SketchExtruder.exportScene();
  }
  const editor = document.getElementById('code-editor');
  const code = editor ? editor.value.trim() : '';
  if (!code) {
    if (window.toast) window.toast('⚠️ No code to export / Aucun code à exporter', 'error');
    return;
  }

  const lang = window.currentLang || 'en';
  const model = window.currentModel || null;

  const modelNameEN = model ? model.en   : 'My 3D Model';
  const modelNameFR = model ? model.fr   : 'Mon Modèle 3D';
  const modelIcon   = model ? model.icon : '🎨';
  const slug = modelNameEN.toLowerCase().replace(/[^a-z0-9]+/g,'-').replace(/^-|-$/g,'') || 'model-3d';
  
  console.log(`[Export] Exporting model: ${modelNameEN} (ID: ${model ? model.id : 'custom'})`);

  const date = new Date().toLocaleDateString('fr-CA');
  const year = new Date().getFullYear();
  const sizeKB = (code.length / 1024).toFixed(1);

  // ── README bilingue ───────────────────────────────────────────────────────
  const readme = `# ${modelIcon} ${modelNameEN} / ${modelNameFR}
> Generated by **Studio 3D/4D Pro** — ${date}

---

## 🇬🇧 ENGLISH — How to open this 3D model

### ✅ Method 1 — Double-click (simplest)
1. Open the folder \`${slug}/\`
2. Double-click **\`index.html\`** (Standalone) or **\`index-split.html\`** (Modular)
3. Your default browser opens the 3D animation automatically ✨

### ✅ Method 2 — Drag & Drop
1. Open **Chrome**, **Firefox**, **Edge** or **Safari**
2. Drag the file **\`index.html\`** into the browser window
3. The 3D model starts immediately

### ✅ Method 3 — Right-click → Open with → Browser

### 📋 Compatible Browsers
| Browser | Min Version | Status |
|---------|-------------|--------|
| Chrome  | 90+         | ✅ Recommended |
| Firefox | 88+         | ✅ Perfect |
| Edge    | 90+         | ✅ Perfect |
| Safari  | 14+         | ✅ Works |
| Opera   | 75+         | ✅ Works |

### 🎮 3D Controls (inside the viewer)
- **Left click + drag** → Rotate the model
- **Right click + drag** → Pan / move view
- **Scroll wheel** → Zoom in / out

### 🔗 Embed in your website
\`\`\`html
<iframe src="index.html" width="800" height="600"
  frameborder="0" allowfullscreen></iframe>
\`\`\`

---

## 🇫🇷 FRANÇAIS — Comment ouvrir ce modèle 3D

### ✅ Méthode 1 — Double-clic (la plus simple)
1. Ouvrez le dossier \`${slug}/\`
2. Double-cliquez sur **\`index.html\`** (Autonome) ou **\`index-split.html\`** (Modulaire)
3. Votre navigateur ouvre l'animation 3D automatiquement ✨

### ✅ Méthode 2 — Glisser-Déposer
1. Ouvrez **Chrome**, **Firefox**, **Edge** ou **Safari**
2. Faites glisser le fichier **\`index.html\`** dans la fenêtre du navigateur
3. Le modèle 3D démarre immédiatement

### ✅ Méthode 3 — Clic droit → Ouvrir avec → Navigateur

### 📋 Navigateurs compatibles
| Navigateur | Version min | Statut |
|------------|-------------|--------|
| Chrome  | 90+  | ✅ Recommandé |
| Firefox | 88+  | ✅ Parfait |
| Edge    | 90+  | ✅ Parfait |
| Safari  | 14+  | ✅ Fonctionne |
| Opera   | 75+  | ✅ Fonctionne |

### 🎮 Contrôles 3D (dans la visionneuse)
- **Clic gauche + déplacer** → Faire pivoter le modèle
- **Clic droit + déplacer** → Déplacer la vue
- **Molette** → Zoom avant / arrière

### 🔗 Intégrer dans votre site web
\`\`\`html
<iframe src="index.html" width="800" height="600"
  frameborder="0" allowfullscreen></iframe>
\`\`\`

---

## 📁 Contenu du package / Package Contents

| Fichier / File | Description |
|----------------|-------------|
| \`index.html\`   | ⭐ **Fichier principal (Autonome) / Main file (Standalone)** — Ouvrez-le ! / Open it! |
| \`index-split.html\` | **Version modulaire / Modular version** (HTML/CSS/JS séparés) |
| \`js/model.js\`  | **Code Javascript / Javascript code** (logique 3D) |
| \`css/style.css\`| **Feuille de style / Stylesheet** (design) |
| \`README.md\`    | Guide bilingue / Bilingual guide |
| \`LICENSE.txt\`  | Licence d'utilisation / License agreement |

---

## ⚙️ Infos Techniques / Technical Info

- **Engine / Moteur :** Three.js r128
- **Format :** Standalone HTML5 + Modular HTML/CSS/JS
- **Internet requis / required :** Oui/Yes — Three.js via CDN
- **Taille / Size :** ~${sizeKB} KB
- **Résolution :** Responsive (adapts to window / s'adapte à la fenêtre)

---

## 📄 © ${year} Studio 3D/4D Pro

Usage personnel & commercial autorisé / Personal & commercial use permitted.
Redistribution des sources interdite / Source file redistribution prohibited.
`;

  // ── LICENSE ───────────────────────────────────────────────────────────────
  const license = `STUDIO 3D/4D PRO — License / Licence
© ${year} Studio 3D/4D Pro  |  Date: ${date}
Model / Modèle: ${modelNameEN} / ${modelNameFR}

=== ENGLISH ===
Licensed for personal and commercial use.
You may embed this file in websites, apps and digital presentations.
You may NOT resell or redistribute the source HTML files as standalone products.
Credit appreciated: "3D Model by Studio 3D/4D Pro" (not required)

=== FRANÇAIS ===
Sous licence pour usage personnel et commercial.
Vous pouvez intégrer ce fichier dans vos sites web, applications et présentations.
Vous ne pouvez PAS revendre ou redistribuer les fichiers HTML source comme produits autonomes.
Crédit apprécié : "Modèle 3D par Studio 3D/4D Pro" (non obligatoire)
`;

  // ── Assemblage ZIP ────────────────────────────────────────────────────────
  if (typeof JSZip === 'undefined') {
    alert('JSZip not loaded. Please reload the page. / JSZip non chargé.');
    return;
  }

  const zip = new JSZip();
  const folder = zip.folder(slug);
  
  // 1. Standalone Index (Original code from editor)
  folder.file('index.html', code); 
  
  // 2. Extract Scripts and Styles for Modular Version
  const scripts = [];
  const styles = [];
  let htmlClean = code;

  try {
    const parser = new DOMParser();
    const doc = parser.parseFromString(code, 'text/html');
    
    // Extract internal scripts
    doc.querySelectorAll('script:not([src])').forEach(el => {
      const content = el.textContent.trim();
      if (content) scripts.push(content);
    });

    // Extract internal styles
    doc.querySelectorAll('style').forEach(el => {
      const content = el.textContent.trim();
      if (content) styles.push(content);
    });

    // Remove internal tags from the clean doc
    doc.querySelectorAll('style').forEach(el => el.remove());
    doc.querySelectorAll('script:not([src])').forEach(el => el.remove());

    // Add external links
    if (styles.length > 0) {
      const link = doc.createElement('link');
      link.rel = 'stylesheet';
      link.href = 'css/style.css';
      if (doc.head) doc.head.appendChild(link);
    }
    if (scripts.length > 0) {
      const script = doc.createElement('script');
      script.src = 'js/model.js';
      if (doc.body) doc.body.appendChild(script);
    }
    
    htmlClean = '<!DOCTYPE html>\n' + doc.documentElement.outerHTML;
  } catch (e) {
    console.warn('DOMParser failed, using raw code for split');
  }

  folder.file('index-split.html', htmlClean); 
  if (scripts.length) folder.folder('js').file('model.js', scripts.join('\n\n'));
  if (styles.length) folder.folder('css').file('style.css', styles.join('\n\n'));
  
  folder.file('README.md', readme);
  folder.file('LICENSE.txt', license);

  const blob = await zip.generateAsync({
    type: 'blob',
    compression: 'DEFLATE',
    compressionOptions: { level: 6 }
  });

  const a = document.createElement('a');
  a.href = URL.createObjectURL(blob);
  a.download = `${slug}-pro-pack.zip`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  setTimeout(() => URL.revokeObjectURL(a.href), 5000);

  const msg = lang === 'fr'
    ? `✅ Export RÉUSSI : Model "${modelNameFR}"`
    : `✅ Export SUCCESS: Model "${modelNameEN}"`;
  if (window.toast) window.toast(msg);
};

// Override binding
setTimeout(() => {
  const ids = ['btn-export-zip', 'exp-zip', 'btn-export-all-formats'];
  ids.forEach(id => {
    const btn = document.getElementById(id);
    if (btn) {
      const n = btn.cloneNode(true);
      btn.parentNode.replaceChild(n, btn);
      n.addEventListener('click', async (e) => {
        e.preventDefault();
        await window.exportZIP();
      });
    }
  });
}, 1000);

console.log('%c ✅ exportZIP Refined (v4)', 'color:#10b981;font-weight:bold;');
